    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        int linha = tabelaList.getSelectedRow();
        String produto = String.valueOf(tabelaList.getValueAt(linha, 0));
        double quantidade = Double.parseDouble(String.valueOf(tabelaList.getValueAt(linha, 1)));
        String data = String.valueOf(tabelaList.getValueAt(linha, 2));
        String obra = String.valueOf(tabelaList.getValueAt(linha, 3));
        int id = numeros.get(linha);
        //System.out.println("Data convertida: " + date);
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, data);
        } else {
            if (altera == null) {
                altera = new alteraEntregas();
                altera.setVisible(true);
                altera.recebendo(quantidade, data, produto, obra, id);
                dispose();
            } else {
                altera.setVisible(true);
                altera.setState(alteraEntregas.NORMAL);
                altera.recebendo(quantidade, data, produto, obra, id);
                dispose();
            }
        }
    }//GEN-LAST:event_AlterarActionPerformed

