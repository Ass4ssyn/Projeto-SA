    private void RecebidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RecebidoActionPerformed
        Entrega ent = new Entrega();
        ConexaoEntrega con = new ConexaoEntrega();
        boolean status;
        int resposta;
        int linha = tabelaList.getSelectedRow();
        String produto = String.valueOf(tabelaList.getValueAt(linha, 0));
        String obra = String.valueOf(tabelaList.getValueAt(linha, 3));
        Double quantEnt = Double.parseDouble(String.valueOf(tabelaList.getValueAt(linha, 1)));

        status = con.conectar();
        if (status == false) {
            System.out.println("CATAPIMBAS");
        } else {
            resposta = con.receber(produto, quantEnt, obra);
            int id = numeros.get(linha);

            if (resposta == 1) {
                boolean operacao = con.excluir(id);
                Excluir.setEnabled(false);
                Alterar.setEnabled(false);
                Recebido.setEnabled(false);
                preencherTabelaCad("");
            } else {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
        con.desconectar();
    }//GEN-LAST:event_RecebidoActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        ConexaoEntrega con = new ConexaoEntrega();
        boolean status = con.conectar();

        if (status == false) {
            JOptionPane.showMessageDialog(null, "Verifique sua conexão");
        } else {

            int linha = tabelaList.getSelectedRow();
            int id = numeros.get(linha);
            boolean operacao = con.excluir(id);
            if (operacao == true) {

                Excluir.setEnabled(false);
                Alterar.setEnabled(false);
                Recebido.setEnabled(false);
                preencherTabelaCad("");
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível realizar a exclusão");
            }
            con.desconectar();
        }
    }//GEN-LAST:event_ExcluirActionPerformed


